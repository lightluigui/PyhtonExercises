cocacola=input('digite algo ')
print('o tipo desse valor é  {} ', type(cocacola))
print('tem espaços?',cocacola.isspace())
print('é numero?',cocacola.isnumeric())
print('é alfabetico?',cocacola.isalpha())
print('é alfanumerico?',cocacola.isalnum())
print('tem maiusculas?',cocacola.isupper())
print('tem minusculas?',cocacola.islower())
'''
isspace so tem espaço
isnumeric so tem numero
isalpha é alfabetico
isalnum é afanumerico
isupper so maiusculas
islower so minusculas

'''

